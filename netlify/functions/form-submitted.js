const { google } = require('googleapis');

// CoachingBio Form Handler — Netlify Function
// Receives form submissions, generates card, sends welcome email

exports.handler = async (event, context) => {
    if (event.httpMethod !== 'POST') {
        return { statusCode: 405, body: 'Method not allowed' };
    }

    try {
        const data = JSON.parse(event.body);
        const name = data.name || '';
        const email = data.email || '';
        const sport = data.sport || '';
        const specialty = data.specialty || '';
        const team = data.team || '';
        const city = data.city || '';
        const phone = data.phone || '';
        const bio = data.bio || '';
        const years = data.years || '';

        if (!name || !email) {
            return { statusCode: 400, body: JSON.stringify({ error: 'Name and email required' }) };
        }

        // Create slug
        const slug = name.toLowerCase()
            .replace(/[^a-z0-9\s-]/g, '')
            .replace(/\s+/g, '-')
            .replace(/-+/g, '-')
            .trim();

        // Card URL
        const cardUrl = `https://coachingbio.com/${slug}.html`;

        // Log submission
        const submission = {
            timestamp: new Date().toISOString(),
            name,
            email,
            sport,
            city,
            slug,
            cardUrl,
            status: 'pending_card_generation'
        };

        console.log('New submission:', JSON.stringify(submission));

        // Return success — card will be generated by build process or manual trigger
        return {
            statusCode: 200,
            body: JSON.stringify({
                success: true,
                message: 'Submission received',
                cardUrl,
                slug
            })
        };

    } catch (error) {
        console.error('Error:', error);
        return {
            statusCode: 500,
            body: JSON.stringify({ error: 'Processing failed' })
        };
    }
};
